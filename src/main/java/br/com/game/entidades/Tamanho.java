package br.com.game.entidades;

public class Tamanho {

    private Integer varW;
    private Integer varH;

    public Tamanho(Integer varW, Integer varH) {
        this.varW = varW;
        this.varH = varH;
    }

    public Integer getVarW() {
        return varW;
    }

    public void setVarW(Integer varW) {
        this.varW = varW;
    }

    public Integer getVarH() {
        return varH;
    }

    public void setVarH(Integer varH) {
        this.varH = varH;
    }
}
