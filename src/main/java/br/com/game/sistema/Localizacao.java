package br.com.game.sistema;

public class Localizacao {

    private int varX;
    private int varY;

    public Localizacao(int varX, int varY) {
        this.varX = varX;
        this.varY = varY;
    }

    public int getVarX() {
        return varX;
    }

    public void setVarX(int varX) {
        this.varX = varX;
    }

    public int getVarY() {
        return varY;
    }

    public void setVarY(int varY) {
        this.varY = varY;
    }
}
